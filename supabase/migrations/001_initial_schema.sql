-- ── Helpers ──────────────────────────────────────────────────────────────────

drop function if exists public.my_team_id() cascade;
create or replace function public.my_team_id()
returns uuid language sql stable security definer as $$
  select team_id from public.profiles where id = auth.uid();
$$;

drop function if exists public.is_super_admin() cascade;
create or replace function public.is_super_admin()
returns boolean language sql stable security definer as $$
  select exists (
    select 1 from public.profiles
    where id = auth.uid() and role = 'SUPER_ADMIN'
  );
$$;

drop function if exists public.is_lead_or_admin() cascade;
create or replace function public.is_lead_or_admin()
returns boolean language sql stable security definer as $$
  select exists (
    select 1 from public.profiles
    where id = auth.uid() and role in ('LEAD', 'SUPER_ADMIN')
  );
$$;


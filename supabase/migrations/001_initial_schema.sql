-- ─── 1. Custom Enums ─────────────────────────────────────────────────────────

do $$
begin
  if not exists (select 1 from pg_type where typname = 'user_role') then
    create type public.user_role as enum ('MEMBER', 'LEAD', 'SUPER_ADMIN');
  end if;
  if not exists (select 1 from pg_type where typname = 'task_status') then
    create type public.task_status as enum ('open', 'pending', 'verified', 'rejected');
  end if;
  if not exists (select 1 from pg_type where typname = 'task_category') then
    create type public.task_category as enum (
      'Registration & Onboarding',
      'Campus Growth & Outreach',
      'Event & Workshop Execution',
      'Community & Content'
    );
  end if;
end;
$$;

-- ─── 2. Base Tables ──────────────────────────────────────────────────────────

-- Teams
create table if not exists public.teams (
  id            uuid primary key default gen_random_uuid(),
  college       text not null,
  tier          int not null default 4 check (tier between 1 and 4),
  lead_id       uuid,  -- FK added after profiles created
  target_users  int not null default 1000,
  created_at    timestamptz default now()
);

-- Profiles (extends auth.users 1-to-1)
create table if not exists public.profiles (
  id            uuid primary key references auth.users(id) on delete cascade,
  full_name     text not null,
  college       text,
  avatar_url    text,
  role          public.user_role not null default 'MEMBER',
  team_id       uuid references public.teams(id),
  tier          int default 4,
  total_points  int not null default 0,
  created_at    timestamptz default now()
);

-- Add foreign key back to profiles from teams.lead_id
do $$
begin
  if not exists (
    select 1 from information_schema.table_constraints
    where constraint_name = 'teams_lead_id_fkey'
  ) then
    alter table public.teams
      add constraint teams_lead_id_fkey
      foreign key (lead_id) references public.profiles(id)
      on delete set null;
  end if;
end;
$$;

-- Invites (reusable or single-use join codes generated by LEAD / SUPER_ADMIN)
create table if not exists public.invites (
  id            uuid primary key default gen_random_uuid(),
  code          text unique not null,
  team_id       uuid references public.teams(id) on delete cascade not null,
  created_by    uuid references public.profiles(id) on delete set null,
  used_by       uuid references public.profiles(id) on delete set null,
  domain_role   text,  -- optional: "Design Lead", "Growth Rep", etc.
  created_at    timestamptz default now(),
  used_at       timestamptz,
  expires_at    timestamptz
);

-- Task Definitions (managed by SUPER_ADMIN)
create table if not exists public.task_definitions (
  id            uuid primary key default gen_random_uuid(),
  title         text not null,
  description   text,
  points        int not null default 100,
  category      public.task_category not null,
  active        boolean not null default true,
  week_assigned int check (week_assigned between 1 and 13),
  created_at    timestamptz default now()
);

-- Task Submissions
create table if not exists public.task_submissions (
  id            uuid primary key default gen_random_uuid(),
  task_id       uuid references public.task_definitions(id) on delete cascade not null,
  user_id       uuid references public.profiles(id) on delete cascade not null,
  status        public.task_status not null default 'open',
  proof_url     text,
  notes         text,
  reviewed_by   uuid references public.profiles(id),
  rejection_reason text,
  points_awarded int default 0,
  submitted_at  timestamptz default now(),
  reviewed_at   timestamptz,
  unique (task_id, user_id)
);

-- Weekly Reports
create table if not exists public.weekly_reports (
  id              uuid primary key default gen_random_uuid(),
  user_id         uuid references public.profiles(id) on delete cascade not null,
  week            int not null check (week between 1 and 13),
  signups_count   int not null default 0,
  reels_posted    int not null default 0,
  active_clubs    int not null default 0,
  big_win         text,
  blocker         text,
  submitted_at    timestamptz default now(),
  unique (user_id, week)
);

-- Reels Tracker
create table if not exists public.reels (
  id            uuid primary key default gen_random_uuid(),
  user_id       uuid references public.profiles(id) on delete cascade not null,
  week          int not null check (week between 1 and 13),
  reel_type     text not null,  -- "launch", "event", "club_highlight", etc.
  url           text,
  posted        boolean not null default false,
  posted_date   date,
  created_at    timestamptz default now()
);

-- Clubs Onboarded
create table if not exists public.clubs (
  id              uuid primary key default gen_random_uuid(),
  team_id         uuid references public.teams(id) on delete cascade not null,
  user_id         uuid references public.profiles(id) on delete cascade not null,
  name            text not null,
  category        text not null,  -- "Tech", "Cultural", "Sports", "Literary"
  president_name  text,
  president_contact text,
  first_event_date date,
  active          boolean not null default true,
  created_at      timestamptz default now()
);

-- Notifications
create table if not exists public.notifications (
  id            uuid primary key default gen_random_uuid(),
  user_id       uuid references public.profiles(id) on delete cascade not null,
  type          text not null,  -- "task_approved", "task_rejected", "invite_accepted", "milestone_unlocked"
  payload       jsonb default '{}'::jsonb,
  read          boolean not null default false,
  created_at    timestamptz default now()
);

